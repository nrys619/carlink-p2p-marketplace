const inferBodyTypes = (name: string): string[] => {
  const upper = name.toUpperCase()
  if (/EV|ELECTRIC|MODEL 3|MODEL S|MODEL X|MODEL Y|LEAF|IONIQ|BOLT|VOLT|I3|I4|I5|I7|EQ/.test(upper)) return ['EV']
  if (/VAN|BUS|WAGON|TOURING|ESTATE|AVANT|VARIANT/.test(upper)) return ['ステーションワゴン']
  if (/SUV|CROSS|TRAIL|PATHFINDER|EXPLORER|ESCAPE|RAV|CR-V|CX-|X[1-9]|Q[1-9]|GL|GLE|GLC|GLA|FORESTER|OUTBACK|LAND|RANGE|DEFENDER|WRANGLER|CHEROKEE|TAHOE|YUKON|ESCALADE|CAYENNE|MACAN|TIGUAN|TOUAREG/.test(upper)) return ['SUV・クロカン']
  if (/COUPE|ROADSTER|SPIDER|CONVERTIBLE|CABRIO|BOXSTER|CAYMAN|CORVETTE|MUSTANG|CAMARO|CHALLENGER|911|GT|Z4/.test(upper)) return ['クーペ']
  if (/MINI|FIT|YARIS|AQUA|NOTE|MARCH|SWIFT|POLO|GOLF|500|COOPER/.test(upper)) return ['コンパクト']
  if (/TRUCK|PICKUP|TACOMA|TUNDRA|F-150|SILVERADO|SIERRA|RAM/.test(upper)) return ['ピックアップ']
  return ['セダン']
}

